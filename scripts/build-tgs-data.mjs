import { readFile, readdir, writeFile, watch } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, "..");
const iconsDir = path.join(projectRoot, "assets", "icons");
const outputPath = path.join(projectRoot, "tgs-data.js");
const watchMode = process.argv.includes("--watch");

function getAliases(fileName) {
  return [
    `./assets/icons/${fileName}`,
    `assets/icons/${fileName}`,
    `./icons/${fileName}`,
    `icons/${fileName}`,
    fileName,
  ];
}

async function buildTgsMap() {
  const files = (await readdir(iconsDir))
    .filter((fileName) => fileName.toLowerCase().endsWith(".tgs"))
    .sort((left, right) => left.localeCompare(right, "en"));

  const animations = {};

  for (const fileName of files) {
    const filePath = path.join(iconsDir, fileName);
    const compressed = await readFile(filePath);
    const aliases = getAliases(fileName);
    const canonicalAlias = aliases[0];

    animations[canonicalAlias] = {
      encoding: "gzip-base64",
      data: compressed.toString("base64"),
    };

    aliases.slice(1).forEach((alias) => {
      animations[alias] = canonicalAlias;
    });
  }

  return {
    animations,
    fileCount: files.length,
  };
}

function buildOutput(animations) {
  const serialized = JSON.stringify(animations);

  return [
    "// Auto-generated by scripts/build-tgs-data.mjs",
    "// Re-run this script after adding new .tgs files when working from file://",
    "window.inlineTgsAnimations=Object.assign({},window.inlineTgsAnimations," +
      serialized +
      ");",
    "",
  ].join("\n");
}

async function writeTgsData() {
  const { animations, fileCount } = await buildTgsMap();
  await writeFile(outputPath, buildOutput(animations), "utf8");
  console.log(`Generated tgs-data.js from ${fileCount} .tgs file(s).`);
}

async function main() {
  await writeTgsData();

  if (!watchMode) return;

  console.log("Watching assets/icons for changes...");

  const watcher = watch(iconsDir);

  for await (const event of watcher) {
    if (!event.filename || !event.filename.toLowerCase().endsWith(".tgs")) {
      continue;
    }

    try {
      await writeTgsData();
    } catch (error) {
      console.error("Failed to rebuild tgs-data.js:", error);
    }
  }
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
